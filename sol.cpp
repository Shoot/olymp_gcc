void solve() {
    ll n; cin >> n;
    ll q; cin >> q;
    vll s(n);
    for (auto &x : s) cin >> x;
    s.push_back(0);
    n = ll(s.size());
    vll rank(n);
    vll sa(n);
    vll lcp(n - 1);
    ll alphabet = 256;
    vector<int> p(n), c(n), cnt(max(alphabet, n), 0);
    for (int i = 0; i < n; i++)
        cnt[s[i]]++;
    for (int i = 1; i < alphabet; i++)
        cnt[i] += cnt[i - 1];
    for (int i = 0; i < n; i++)
        p[--cnt[s[i]]] = i;
    c[p[0]] = 0;
    int classes = 1;
    for (int i = 1; i < n; i++) {
        if (s[p[i]] != s[p[i - 1]])
            classes++;
        c[p[i]] = classes - 1;
    }
    vector<int> pn(n), cn(n);
    for (int h = 0; (1 << h) < n; ++h) {
        for (int i = 0; i < n; i++) {
            pn[i] = p[i] - (1 << h);
            if (pn[i] < 0)
                pn[i] += n;
        }
        fill(cnt.begin(), cnt.begin() + classes, 0);
        for (int i = 0; i < n; i++)
            cnt[c[pn[i]]]++;
        for (int i = 1; i < classes; i++)
            cnt[i] += cnt[i - 1];
        for (int i = n - 1; i >= 0; i--)
            p[--cnt[c[pn[i]]]] = pn[i];
        cn[p[0]] = 0;
        classes = 1;
        for (int i = 1; i < n; i++) {
            pair<int, int> cur = {c[p[i]], c[(p[i] + (1 << h)) % n]};
            pair<int, int> prev = {c[p[i - 1]], c[(p[i - 1] + (1 << h)) % n]};
            if (cur != prev)
                ++classes;
            cn[p[i]] = classes - 1;
        }
        c.swap(cn);
    }
    for (ll i = 0; i < n; i += 1) {
        rank[i] = c[i];
        sa[c[i]] = i;
    }
    ll k = 0;
    for (ll i = 0; i < n; i += 1, k ? k -= 1 : 0) {
        if (rank[i] == n-1) {
            k = 0;
            continue;
        }
        ll j = sa[rank[i]+1];
        while (i+k < n && j+k < n && s[i+k] == s[j+k]) k++;
        lcp[rank[i]] = k;
    }
    stack<ll> st;
    ll maxi = n-1;
    ll best_l = 0;
    ll best_r = n-2;
    for (ll i = 0; i < ll(lcp.size()); i += 1) {
        while (!st.empty() && lcp[st.top()] > lcp[i]) {
            ll x = st.top();
            st.pop();
            ll length;
            if (!st.empty()) {
                length = i-st.top();
            } else {
                length = i+1;
            }
            ll val = lcp[x];
            if (val*length > maxi) {
                maxi = val*length;
                ll where = sa[x];
                best_l = where;
                best_r = where+val-1;
            }
        }
        st.push(i);
    }
    while (!st.empty()) {
        ll x = st.top();
        ll val = lcp[x];
        st.pop();
        ll length;
        if (!st.empty()) {
            length = lcp.size()-st.top();
        } else {
            length = lcp.size()+1;
        }
        if (val*length > maxi) {
            maxi = val*length;
            ll where = sa[x];
            best_l = where;
            best_r = where+val-1;
        }
    }
    cout << maxi << endl;
    cout << best_r-best_l+1 << endl;
    for (ll i = best_l; i <= best_r; i += 1) {
        cout << s[i] << ' ';
    }cout << endl;
}

